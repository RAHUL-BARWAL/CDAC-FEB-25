public class BooleanLiteralsDemo {
    public static void main(String[] args) {
        boolean isJavaFun = true;
        boolean isCodingHard = false;
        
        // Using boolean literals in if-else statements
        if (isJavaFun) {
            System.out.println("Java is fun to learn!");
        } else {
            System.out.println("Java is not fun.");
        }
        
        if (isCodingHard) {
            System.out.println("Coding is difficult.");
        } else {
            System.out.println("Coding is enjoyable!");
        }
    }
}
